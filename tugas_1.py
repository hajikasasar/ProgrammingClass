A = int(input("Masukan Nilai pertama = "))
B = int(input("Masukan Nilai kedua = "))
C = int(input("Masukan Nilai ketiga = "))
D = A + B + C
E = A - B - C
F = A * B * C
G = A //B // C

print(" %d + %d + %d = "%(A,B,C),D)
print(" %d - %d - %d = "%(A,B,C),E)
print(" %d x %d x %d = "%(A,B,C),F)
print(" %d / %d / %d = "%(A,B,C),G)